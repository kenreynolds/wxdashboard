export function convertCelsiusToFahrenheit(temp: number) {
    return temp * 1.8 + 32;
  }

export function convertMetersPerSecondToMilePerHour(speed: number) {
    return speed * 2.236936;
  }

export function convertMetersToMiles(distance: number) {
    return distance * 0.00062137;
  }

export function convertPascalsToMillibar(pressure: number) {
    return pressure / 100;
  }